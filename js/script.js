"use strict"

//==================================MAP=========================================================================

// Как и обычный объект это коллекция пар КЛЮЧ - ЗНАЧЕНИЕ. 
// Основное отличие в том, что КЛЮЧОМ МОЖЕТ БЫТЬ НЕ ТОЛЬКО СТРОКА.
// К примеру число, булево значения, или даже другой объект.

//==================================SET=========================================================================

// Это особая коллекция, без ключей, только значения.
// Особенность в том, что set ХРАНИТ ТОЛЬКО УНИКАЛЬНЫЕ ЗНАЧЕНИЯ.
// При попытке записать в него уже существующее значени, оно не запишется.


//==================================WeakMAP=========================================================================

// Особый вид Map-a
// Его ключами могут быть ТОЛЬКО объекты.
// ВАЖНО! Его пара ключ-свойство, существует только до тех пор, пока объект, который послужил ключом существует где-нибудь еще (в переменной, как элемент другого объекта/массива и т.д)
// Это позволяет использовать WeakMap как кэш или самоочищаюшееся хранилище, т.к. когда пропадет объект, являющийся ключом в WeakMAP, пропадут и все пары ключ-свойство с этим объектом.

//==================================WeakSET=========================================================================

// Особый вид Set-a
// В его коллекции могут быть ТОЛЬКО объекты.
// ВАЖНО! Его элемент коллекции, существует только до тех пор, пока объект, который мы "положили" в WeakSet существует где-нибудь еще (в переменной, как элемент другого объекта/массива и т.д)
// В отличии от WeakMap, мы не можем описать объект лежащий в WeakSet-е, его область применения ограничена в основном тем что мы можем узнать, есть ли что-то в WeakSet-е или нет.


